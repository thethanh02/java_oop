    private void sortByBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sortByBoxActionPerformed
        // TODO add your handling code here:
        String choice = conditionToSort.getItemAt(conditionToSort.getSelectedIndex());
        if (choice.equals("theo ten nhan vien")) {
            Collections.sort(listChamCong, (o1, o2) -> {
                return o1.getNhanVien().getName().compareTo(o2.getNhanVien().getName());
            });
        } else {
            Collections.sort(listChamCong, (o1, o2) -> {
                return o1.getPhongBan().getName().compareTo(o2.getPhongBan().getName());
            });
        }
    }//GEN-LAST:event_sortByBoxActionPerformed

    private void addChamCongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addChamCongActionPerformed
        // TODO add your handling code here:
        int indexNhanVien = getIdNV.getSelectedIndex();
        int indexPhongBan = getIdPB.getSelectedIndex();
        NhanVien nhanVien = listNhanVien.get(indexNhanVien);
        PhongBan phongBan = listPhongBan.get(indexPhongBan);
        try {
            int workDays = Integer.parseInt(getWorkDays.getText());
            if (workDays < 0 || workDays > 30) {
                JOptionPane.showMessageDialog(null, "nhap sai so ngay lam");
            }
            ChamCong chamCong = new ChamCong(nhanVien, phongBan, workDays);
            int count = 0;
            for (ChamCong cc:listChamCong) {
                if (chamCong.trungNhau(cc)) {
                    int x = workDays + cc.getWorkDays();
                    if (x > 30) {
                        JOptionPane.showMessageDialog(null, "so ngay lam k duoc vuot qua 30");
                        return;
                    } else {
                        cc.setWorkDays(x);
                        tableChamCong.setValueAt(x, count, 4);
                        return;
                    }
                }
                count++;
                if (chamCong.dieuKien(cc)) {
                    JOptionPane.showMessageDialog(null, "1 nhan vien khong the lam 2 phong khac nhau");
                    return;
                }
            }
            tableChamCong.addRow(chamCong.toObjects());
            listChamCong.add(chamCong);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "So ngay lam phai nhap bang so");
        }
    }//GEN-LAST:event_addChamCongActionPerformed

    private void addPhongBanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPhongBanActionPerformed
        // TODO add your handling code here:
        String name = getNamePhongBan.getText();
        String descript = getDescript.getText();
        String coefficient = getCoefficient.getText();

        int temp = listPhongBan.size() - 1;
        if (temp != -1) {
            PhongBan.setCurrentId(listPhongBan.get(temp).getId());
        }

        try {
            double h = Double.parseDouble(coefficient);
            PhongBan phongBan = new PhongBan(name, descript, h);
            for (PhongBan pb:listPhongBan) {
                if (phongBan.trungNhau(pb)) {
                    throw new trungNhauException();
                }
            }
            tablePhongBan.addRow(phongBan.toObjects());
            listPhongBan.add(phongBan);
            loadDataToBox();
        } catch (deTrongException e) {
            JOptionPane.showMessageDialog(null, "khong duoc de trong");
        } catch (heSoCongViecException e) {
            JOptionPane.showMessageDialog(null, "Nhap sai he so cong viec");
        } catch (trungNhauException e) {
            JOptionPane.showMessageDialog(null, "trung phong ban");
        }
    }//GEN-LAST:event_addPhongBanActionPerformed

    private void addNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addNhanVienActionPerformed
        // TODO add your handling code here:
        try {
            String name = getName.getText();
            String address = getAddress.getText();
            String phone = getPhone.getText();
            int wage = getWage.getSelectedIndex() + 1;

            int temp = listNhanVien.size() - 1;
            if (temp != -1) {
                NhanVien.setCurrentId(listNhanVien.get(temp).getId());
            }

            NhanVien nhanVien = new NhanVien(name, address, phone, wage);
            for (NhanVien nv:listNhanVien) {
                if (nhanVien.trungNhau(nv)) {
                    throw new trungNhauException();
                }
            }
            tableNhanVien.addRow(nhanVien.toObjects());
            listNhanVien.add(nhanVien);
            loadDataToBox();
        } catch (deTrongException ex) {
            JOptionPane.showMessageDialog(null, "khong duoc de trong");
        } catch (hoTenException ex) {
            JOptionPane.showMessageDialog(null, "ho ten phai day du");
        } catch (soDienThoaiException ex) {
            JOptionPane.showMessageDialog(null, "so dien thoai sai");
        } catch (trungNhauException ex) {
            JOptionPane.showMessageDialog(null, "trung nhan vien");
        }
    }//GEN-LAST:event_addNhanVienActionPerformed

