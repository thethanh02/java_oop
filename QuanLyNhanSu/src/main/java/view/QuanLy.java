    private void loadDataToTable() {
        FileIO.readFile(listNhanVien, fileNhanVien);
        FileIO.readFile(listPhongBan, filePhongBan);
        FileIO.readFile(listChamCong, fileChamCong);
        for (NhanVien nhanVien:listNhanVien) {
            tableNhanVien.addRow(nhanVien.toObjects());
        }
        for (PhongBan phongBan:listPhongBan) {
            tablePhongBan.addRow(phongBan.toObjects());
        }
        for (ChamCong chamCong:listChamCong) {
            tableChamCong.addRow(chamCong.toObjects());
        }
    }
    
    private void loadDataToBox() {
        getIdNV.removeAllItems();
        getIdPB.removeAllItems();
        for (NhanVien nhanVien:listNhanVien) {
            getIdNV.addItem(nhanVien.getId()+"");
        }
        for (PhongBan phongBan:listPhongBan) {
            getIdPB.addItem(phongBan.getId()+"");
        }
    }
    
    private void saveDataToFile(JButton jb, ArrayList list, String file) {
        jb.addActionListener((e) -> {
            FileIO.writeFile(list, file);
            JOptionPane.showMessageDialog(null, "Da luu vao "+file);
        });
    }
